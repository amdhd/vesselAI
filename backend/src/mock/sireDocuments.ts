export const MOCK_SIRE_DOCUMENTS: MockSireDocument[] = [
  ...merdekaDocuments,
  ...kertehDocuments,
  ...osvDocuments,
];

export const getSireDocumentsByVesselId = (vesselId: string): MockSireDocument[] => {
  return MOCK_SIRE_DOCUMENTS.filter((d) => d.vesselId === vesselId);
};

export const getExpiringDocuments = (daysThreshold: number = 60): MockSireDocument[] => {
  const threshold = new Date();
  threshold.setDate(threshold.getDate() + daysThreshold);
  return MOCK_SIRE_DOCUMENTS.filter((d) => {
    if (!d.expiryDate) return false;
    const expiry = new Date(d.expiryDate);
    return expiry <= threshold && d.status !== 'expired';
  });
};
